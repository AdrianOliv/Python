# Programa com função que calcula a área a partir das dimensões passadas.
# Dimensões Comprimento e Largura.

def area(c, l):
    return c*l

print(f'\nA área do terreno A, medindo 6m x 4m é {area(6, 4)} m²')
print(f'A área do terreno B, medindo 10m x 8m é {area(10, 8)} m²')
print(f'A área do terreno C, medindo 12m x 26m é {area(12, 26)} m²\n')